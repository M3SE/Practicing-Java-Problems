public int sumDouble(int a, int b) {

    int sum = a + b;

    if (a == b) {
        sum = sum * 2;
    }

    return sum;
}

/* The method takes two integer parameters a and b then calculates their sum and stores it in the variable sum.
It checks if a and b are equal. If they are, it doubles the value of sum it'll then return the final value of sum. */